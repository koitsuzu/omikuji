// Stage 4: Drawing
const drawButton = document.getElementById('drawButton');
const omikujiBox = document.getElementById('omikujiBox');
const omikujiStick = document.getElementById('omikujiStick');
const resultModal = document.getElementById('resultModal');
const closeButton = document.getElementById('closeButton');
const interpretBtn = document.getElementById('interpretBtn');
const detailsSection = document.getElementById('detailsSection');

function drawFortune() {
    drawButton.disabled = true;
    omikujiBox.classList.add('shaking');
    speech.textContent = currentLang === 'cn' ? "誠心祈禱，籤詩就要出來了..." : "誠心祈禱，籤詩就要出來了..."; // Keep Chinese for speech

    setTimeout(() => {
        omikujiBox.classList.remove('shaking');
        omikujiStick.classList.add('stick-emerge');

        setTimeout(() => {
            showResult();
        }, 800);
    }, 1500);
}

function showResult() {
    const randomIndex = Math.floor(Math.random() * fortuneData.length);
    currentFortune = fortuneData[randomIndex];
    updateResultDisplay();

    resultModal.classList.add('show');
    speech.textContent = "你的運勢出爐了！點擊解籤看看詳細內容吧。";
}

interpretBtn.addEventListener('click', () => {
    detailsSection.classList.remove('hidden');
    interpretBtn.classList.add('hidden');
    closeButton.classList.remove('hidden');
});

function resetDraw() {
    resultModal.classList.remove('show');
    omikujiStick.classList.remove('stick-emerge');
    drawButton.disabled = false;
    detailsSection.classList.add('hidden');
    interpretBtn.classList.remove('hidden');
    closeButton.classList.add('hidden');
    currentFortune = null;

    currentStage = 0;
    updateStage(0);
    washBtn.disabled = true;
    offerBtn.disabled = true;
    prayerBtn.disabled = true;
    coin.classList.remove('coin-drop');
}

drawButton.addEventListener('click', drawFortune);
closeButton.addEventListener('click', resetDraw);
omikujiBox.addEventListener('click', () => {
    if (!drawButton.disabled) drawFortune();
});

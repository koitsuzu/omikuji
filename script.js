// DOM Elements
const stages = ['stepWashing', 'stepOffering', 'stepPrayer', 'stepDrawing'];
let currentStage = 0;

const speech = document.getElementById('mascotSpeech');
const speechLib = {
    stepWashing: "請先洗洗手，淨化身心喔！",
    stepOffering: "投下一枚 5 元硬幣，與神明結個緣吧。",
    stepPrayer: "敲響鈴鐺，把你的願望告訴神明。",
    stepDrawing: "最後，虔誠地抽取你的運勢籤吧！"
};

let currentLang = 'jp'; // 'jp' or 'cn'
let currentFortune = null;

function updateStage(index) {
    console.log(`--- Transitioning to Stage: ${index} (${stages[index]}) ---`);
    document.querySelectorAll('.flow-step').forEach(s => {
        s.classList.remove('active');
        s.style.display = 'none'; // Force hide
    });
    const nextStep = document.getElementById(stages[index]);
    if (nextStep) {
        nextStep.classList.add('active');
        nextStep.style.display = 'block'; // Force show
        speech.textContent = speechLib[stages[index]];
    } else {
        console.error(`Stage element not found: ${stages[index]}`);
    }
}

// Language Toggle Logic
const langToggle = document.getElementById('langToggle');
langToggle.addEventListener('change', () => {
    currentLang = langToggle.checked ? 'cn' : 'jp';
    if (currentFortune) {
        updateResultDisplay();
    }
});

function updateResultDisplay() {
    if (!currentFortune) return;

    const f = currentFortune;
    const isCn = currentLang === 'cn';

    const rankText = isCn ? (f.rank_cn || f.rank) : f.rank;
    const noPrefix = f.no ? (isCn ? `${f.no}番 ` : `${f.no}番 `) : '';

    document.getElementById('fortuneRank').textContent = `${noPrefix}${rankText}`;
    document.getElementById('poetryText').textContent = isCn ? (f.poetry_cn || f.poetry) : f.poetry;

    // Details
    document.getElementById('wishDetail').textContent = isCn ? (f.wish_cn || f.wish) : f.wish;
    document.getElementById('personDetail').textContent = isCn ? (f.person_cn || f.person) : f.person;
    document.getElementById('lostDetail').textContent = isCn ? (f.lost_cn || f.lost) : f.lost;
    document.getElementById('travelDetail').textContent = isCn ? (f.travel_cn || f.travel) : f.travel;
    document.getElementById('businessDetail').textContent = isCn ? (f.business_cn || f.business) : f.business;
    document.getElementById('studyDetail').textContent = isCn ? (f.study_cn || f.study) : f.study;

    // Update labels if needed
    const labels = document.querySelectorAll('.detail-item .label');
    const labelMaps = {
        jp: ["願事", "待人", "失物", "旅行", "商売", "學問"],
        cn: ["願望", "貴人", "失物", "旅行", "生意", "學業"]
    };
    labels.forEach((lbl, idx) => {
        lbl.textContent = labelMaps[currentLang][idx];
    });
}

